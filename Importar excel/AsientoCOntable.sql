    V_TRCONTABLE_ ALIAS_0
    LEFT OUTER JOIN V_TIPOTRANSACCION_ ALIAS_3 ON ALIAS_0.TIPOTRORIGEN_ID = ALIAS_3.ID
    LEFT OUTER JOIN V_TREXTENSION_ ALIAS_4 ON ALIAS_0.TREXTENSION_ID = ALIAS_4.ID
    LEFT OUTER JOIN V_CENTROCOSTOS_ ALIAS_5 ON ALIAS_0.CENTROCOSTOS_ID = ALIAS_5.ID
    LEFT OUTER JOIN V_PERIODO_ ALIAS_2 ON ALIAS_0.BO_PLACE_ID = ALIAS_2.ASIENTOS_ID
    LEFT OUTER JOIN V_EJERCICIO_ ALIAS_1 ON ALIAS_2.BO_PLACE_ID = ALIAS_1.PERIODOS_ID
    inner
WHERE
    ALIAS_1.ID IN (
        '{376A812C-E547-458A-8E0D-F32AEE33DFFF}',
        '{77063E9F-8B97-4FDA-BB9E-AC65A8104ABF}',
        '{D22F95A6-D71A-4E4E-A06F-99BD97252445}',

    ALIAS_0.NOMBRE ALIAS_0_NOMBRE,
    ALIAS_0.BO_PLACE_ID ALIAS_0_BO_PLACE_ID,
    ALIAS_1.DESCRIPCION ALIAS_1_DESCRIPCION,
    ALIAS_2.DESCRIPCION ALIAS_2_DESCRIPCION,
    ALIAS_2.ESTADO ALIAS_2_ESTADO,
    ALIAS_0.NUMERODOCUMENTO ALIAS_0_NUMERODOCUMENTO,
    ALIAS_0.FECHAAPLICACION ALIAS_0_FECHAAPLICACION,
    ALIAS_0.ESTADO ALIAS_0_ESTADO,
